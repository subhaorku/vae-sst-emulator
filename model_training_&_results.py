def generate_many_sst_samples(model, num_samples=1980, latent_dim=256, batch_size=64):
    model.eval()
    samples = []

    for i in range(0, num_samples, batch_size):
        cur_bs = min(batch_size, num_samples - i)
        z = torch.randn(cur_bs, latent_dim, device=next(model.parameters()).device)
        samples.append(model.decoder(z).cpu())

    return torch.cat(samples, dim=0)

